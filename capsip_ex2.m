% SIP OBJECTIVE FUNCTION
objfun = @(x) x(7);
w1 = 0:0.1:1;
w2 = 1:0.1:2.5;
[W1, W2] = meshgrid(w1, w2);

% d(w): target function
d = log(W1 + W2).*sin(W1);
surf(W1, W2, d); 
hold on;

x = [ 0.0000    1.0000   -0.4828   -0.6145    0.6170    0.5470    0.5178]; % 0.1
x = [-0.0000    1.0000   -0.4828   -0.6730    0.6851    0.1635    0.5178]; % 0.01
x = [-0.0000    1.0000   -0.4828   -0.6729    0.6850    0.1637    0.5178]; % 0.005
F = x(1) +          ...
    x(2).*W2 +      ...
    x(3).*W2.^2 +   ...
    x(4).*W1 +      ...
    x(5).*W1.*W2 +  ...
    x(6).*W1.^2;
surf(W1, W2, F);
